#include "../mandatory/cub.h"

int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	len;

	i = 0;
	len = ft_strlen(s1);
	while (i < len)
	{
		if (s1[i] != s2[i])
			return (1);
		i++;
	}
	return (0);
}
